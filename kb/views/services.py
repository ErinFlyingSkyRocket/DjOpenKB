def mark_article_image_deleted(filename, actor=None, reason="", record_activity=True):
    """Record image deletion without editing the original upload log.

    ArticleImageUploadLog is append-only and records the upload event. Deletion
    events are appended into ActivityLog instead of updating deleted_at/delete_reason
    on the original upload row. Views that already call log_activity() can pass
    record_activity=False to avoid duplicate ActivityLog rows.
    """
    filename = safe_uploaded_filename(filename)
    if not filename:
        return

    logger.info(
        "Article image deletion noted without editing append-only upload log: filename=%s actor=%s reason=%s",
        filename,
        getattr(actor, "pk", None),
        reason or "",
    )

    if not record_activity:
        return

    try:
        actor_is_authenticated = bool(getattr(actor, "is_authenticated", False))
        ActivityLog.objects.create(
            event_type=ActivityLog.EventType.IMAGE_DELETED,
            user=actor if actor_is_authenticated else None,
            username=actor.get_username() if actor_is_authenticated else "",
            details={
                "filename": filename,
                "reason": reason or "",
                "source": "append_only_image_delete_marker",
            },
        )
    except Exception:
        logger.exception("Unable to append image deletion ActivityLog for %s", filename)

def find_stray_uploaded_files(min_age_minutes=1440):
    """Return uploaded files that are not referenced anywhere.

    The minimum age protects someone who is currently editing an article: a
    file uploaded seconds ago may not be saved into Markdown yet.
    """
    init_openkb_storage()
    upload_dir = get_openkb_uploads_dir().resolve()
    referenced = get_all_referenced_uploaded_files()
    cutoff_time = timezone.now().timestamp() - (min_age_minutes * 60)

    stray_files = []

    if not upload_dir.exists():
        return stray_files

    for file_path in upload_dir.iterdir():
        if not file_path.is_file():
            continue

        filename = file_path.name
        if filename in referenced:
            continue

        try:
            stat = file_path.stat()
        except OSError:
            continue

        if stat.st_mtime > cutoff_time:
            continue

        extension = file_path.suffix.lower().lstrip(".") or "(no extension)"
        is_previewable_image = file_path.suffix.lower() in {".png", ".jpg", ".jpeg", ".gif", ".webp"}

        upload_log = get_article_image_upload_log(filename)
        uploader_display = ""
        uploader_email = ""
        uploader_account_type = ""
        uploaded_at = None

        if upload_log:
            uploader_display = upload_log.uploader_display
            uploader_email = upload_log.uploader_email_snapshot
            uploader_account_type = upload_log.uploader_account_type_snapshot
            uploaded_at = upload_log.uploaded_at

        stray_files.append({
            "filename": filename,
            "url": article_image_url(filename),
            "extension": extension,
            "is_previewable_image": is_previewable_image,
            "size_bytes": stat.st_size,
            "size_kb": round(stat.st_size / 1024, 1),
            "modified_at": datetime.fromtimestamp(stat.st_mtime),
            "path": file_path,
            "upload_log": upload_log,
            "uploader_display": uploader_display,
            "uploader_email": uploader_email,
            "uploader_account_type": uploader_account_type,
            "uploaded_at": uploaded_at,
        })
    stray_files.sort(key=lambda item: item["modified_at"], reverse=True)
    return stray_files


def clean_wiki_title(file_path):
    return file_path.stem.replace("-", " ").replace("_", " ").title()


