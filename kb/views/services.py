def record_article_session_view(request, article):
    """Increment an article view count once per browser session.

    This prevents simple page refreshes from repeatedly increasing the counter.
    A different browser/session can still count as a new view.
    """
    if not article or not article.pk:
        return

    session_key = "viewed_suggested_article_ids"
    viewed_ids = request.session.get(session_key, [])
    article_key = str(article.pk)

    if article_key in viewed_ids:
        return

    SuggestedArticle.objects.filter(pk=article.pk).update(view_count=F("view_count") + 1)
    article.view_count = (article.view_count or 0) + 1

    viewed_ids.append(article_key)
    request.session[session_key] = viewed_ids[-1000:]
    request.session.modified = True


def get_openkb_wiki_articles(sort_by_views=False):
    """Return public article cards from published Django articles only.

    Raw /wiki/*.md files are no longer treated as public pages. OpenKB still
    uses wiki/sources internally for AI indexing, but the website links to the
    safe Django article route instead of exposing Markdown file paths.
    """
    articles = []

    queryset = SuggestedArticle.objects.select_related("owner").filter(
        status=SuggestedArticle.Status.PUBLISHED,
    )

    for suggested in queryset:
        modified_at = timezone.localtime(suggested.updated_at).strftime("%Y-%m-%d %H:%M")
        articles.append({
            "title": suggested.title,
            "type": "Article",
            "date": modified_at,
            "views": suggested.view_count or 0,
            "url": suggested.public_url,
            "path": "",
            "raw_markdown": build_article_markdown(suggested),
            "author": suggested.author_display,
            "keywords": suggested.keyword_list,
            "suggested_id": suggested.pk,
        })

    if sort_by_views:
        articles.sort(
            key=lambda item: (item.get("views") or 0, item.get("date") or ""),
            reverse=True,
        )
    else:
        articles.sort(key=lambda item: item["date"], reverse=True)
    return articles


def is_ldap_managed_user(user):
    """Return True only when the stored profile marks this as an AD account.

    Do not infer AD status from email domain. A local Django user may have a
    company-style email such as alice@openkb.local and must still be allowed to
    use local password management.
    """
    profile = getattr(user, "kb_profile", None)
    return bool(profile and profile.is_ldap_type)


def get_user_profile(user):
    if not user.is_authenticated:
        return None

    profile, created = UserProfile.objects.get_or_create(user=user)

    # Keep legacy role values consistent with the explicit source column.
    # The source column decides Local vs AD. The account_type field remains for
    # role/backward-compatibility, so it is aligned to Local User/Admin or AD User/Admin.
    role_is_admin = profile.account_type in {
        UserProfile.AccountType.ADMIN,
        UserProfile.AccountType.LDAP_ADMIN,
    }
    if profile.auth_source == UserProfile.AuthSource.ACTIVE_DIRECTORY:
        desired_account_type = UserProfile.AccountType.LDAP_ADMIN if role_is_admin else UserProfile.AccountType.LDAP_USER
    else:
        desired_account_type = UserProfile.AccountType.ADMIN if role_is_admin else UserProfile.AccountType.USER
    if profile.account_type != desired_account_type:
        profile.account_type = desired_account_type
        profile.save(update_fields=["account_type", "updated_at"])

    # Keep existing superuser/staff accounts visible as Admin unless already LDAP admin.
    if (user.is_superuser or user.is_staff) and profile.account_type not in {
        UserProfile.AccountType.ADMIN,
        UserProfile.AccountType.LDAP_ADMIN,
    }:
        profile.account_type = UserProfile.AccountType.ADMIN
        profile.save(update_fields=["account_type", "updated_at"])

    return profile


def user_can_access_main_site(user):
    if not user.is_authenticated or not user.is_active:
        return False

    profile = get_user_profile(user)
    return bool(profile and profile.can_access_main_site)


def main_site_login_required(view_func):
    @wraps(view_func)
    @login_required
    def wrapper(request, *args, **kwargs):
        if not user_can_access_main_site(request.user):
            logout(request)
            return redirect("home")
        return view_func(request, *args, **kwargs)

    return wrapper


def get_account_type_display(user):
    profile = get_user_profile(user)
    if not profile:
        return ""

    if profile.is_ldap_type:
        if profile.is_admin_type:
            return "AD Admin"
        return "AD User"

    if profile.is_admin_type:
        return "Local Admin"
    return "Local User"

