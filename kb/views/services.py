def record_article_session_view(request, article):
    """Increment an article view count once per browser session.

    This prevents simple page refreshes from repeatedly increasing the counter.
    A different browser/session can still count as a new view.
    """
    if not article or not article.pk:
        return

    session_key = "viewed_suggested_article_ids"
    viewed_ids = request.session.get(session_key, [])
    article_key = str(article.pk)

    if article_key in viewed_ids:
        return

    SuggestedArticle.objects.filter(pk=article.pk).update(view_count=F("view_count") + 1)
    article.view_count = (article.view_count or 0) + 1

    viewed_ids.append(article_key)
    request.session[session_key] = viewed_ids[-1000:]
    request.session.modified = True

    log_activity(
        request,
        ActivityLog.EventType.ARTICLE_VIEWED,
        article=article,
        details={"view_count_after": article.view_count},
    )


def get_openkb_wiki_articles(sort_by_views=False):
    """Return public article cards from published Django articles only.

    Raw /wiki/*.md files are no longer treated as public pages. OpenKB still
    uses wiki/sources internally for AI indexing, but the website links to the
    safe Django article route instead of exposing Markdown file paths.
    """
    articles = []

    queryset = SuggestedArticle.objects.select_related("owner").filter(
        status=SuggestedArticle.Status.PUBLISHED,
    ).annotate(
        db_helpful_vote_count=Count(
            "votes",
            filter=Q(votes__value=ArticleVote.VoteValue.UP),
        )
    )

    for suggested in queryset:
        modified_at = timezone.localtime(suggested.updated_at).strftime("%Y-%m-%d %H:%M")
        articles.append({
            "title": suggested.title,
            "type": "Article",
            "date": modified_at,
            "views": suggested.view_count or 0,
            "likes": getattr(suggested, "db_helpful_vote_count", 0) or 0,
            "url": suggested.public_url,
            "path": "",
            "raw_markdown": build_article_markdown(suggested),
            "author": suggested.author_display,
            "keywords": suggested.keyword_list,
            "suggested_id": suggested.pk,
        })

    if sort_by_views:
        articles.sort(
            key=lambda item: (item.get("views") or 0, item.get("date") or ""),
            reverse=True,
        )
    else:
        articles.sort(key=lambda item: item["date"], reverse=True)
    return articles


def is_ldap_managed_user(user):
    """Return True only when the account is explicitly marked as AD-managed.

    Do not infer AD ownership from the username or email domain. A local Django
    user may legitimately use an email such as alice@openkb.local and should
    still be allowed to change their local password.
    """
    profile = getattr(user, "kb_profile", None)
    if not profile:
        return False

    if hasattr(profile, "AuthSource"):
        return profile.auth_source == profile.AuthSource.AD

    # Compatibility fallback for older databases/code during deployment.
    return bool(getattr(profile, "is_ldap_type", False))


def get_user_profile(user):
    if not user.is_authenticated:
        return None

    profile, created = UserProfile.objects.get_or_create(user=user)

    # Keep superusers and Admin Users role accounts visible as Admin unless already LDAP admin.
    # Article Manager accounts may also be staff for limited Django admin access,
    # so staff status alone must not promote someone to full Admin.
    try:
        has_admin_role = user.groups.filter(name="Admin Users").exists()
    except Exception:
        has_admin_role = False

    if (user.is_superuser or has_admin_role) and profile.account_type not in {
        UserProfile.AccountType.ADMIN,
        UserProfile.AccountType.LDAP_ADMIN,
    }:
        profile.account_type = (
            UserProfile.AccountType.LDAP_ADMIN
            if profile.auth_source == UserProfile.AuthSource.AD
            else UserProfile.AccountType.ADMIN
        )
        profile.save(update_fields=["account_type", "updated_at"])

    return profile


def user_can_access_main_site(user):
    if not user.is_authenticated or not user.is_active:
        return False

    if user_has_disabled_role(user):
        return False

    profile = get_user_profile(user)
    return bool(profile and profile.can_access_main_site)


def main_site_login_required(view_func):
    @wraps(view_func)
    @login_required
    def wrapper(request, *args, **kwargs):
        if not user_can_access_main_site(request.user):
            logout(request)
            return redirect("root_login")
        return view_func(request, *args, **kwargs)

    return wrapper


def get_account_type_display(user):
    profile = get_user_profile(user)
    if not profile:
        return "Guest"

    if profile.account_type == UserProfile.AccountType.LDAP_USER:
        return "Domain User"
    if profile.account_type == UserProfile.AccountType.LDAP_ADMIN:
        return "Domain Admin"
    if profile.account_type == UserProfile.AccountType.USER:
        return "Local User"
    if profile.account_type == UserProfile.AccountType.ADMIN:
        return "Local Admin"

    return profile.get_account_type_display()


def format_profile_display_name(user):
    full_name = user.get_full_name().strip()
    return full_name or user.get_username()


def paginate_articles(request, articles, per_page=20, page_param="page"):
    """Paginate article lists safely for the index/search page.

    page_param lets homepage tabs keep separate pagination states while the
    normal search page continues using the default ?page= parameter.
    """
    paginator = Paginator(articles, per_page)
    page_number = request.GET.get(page_param, 1)

    try:
        page_obj = paginator.page(page_number)
    except PageNotAnInteger:
        page_obj = paginator.page(1)
    except EmptyPage:
        page_obj = paginator.page(paginator.num_pages)

    return page_obj


