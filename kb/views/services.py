def remove_openkb_internal_metadata(markdown_text):
    """Remove Django/OpenKB sync metadata before public display/search/AI snippets.

    The Markdown source files keep small internal markers so Django can reconcile
    generated files, but those markers should never appear in search results,
    article snippets, or OpenKB AI responses. This also handles already-flattened
    variants such as "!generated by: django suggested article; article id: ...".
    """
    text = markdown_text or ""

    # Remove the original hidden marker comment.
    text = re.sub(
        r"^\s*<!--\s*generated[_\s-]*by\s*:\s*django[-_\s]*suggested[-_\s]*article[^>]*-->\s*",
        "",
        text,
        flags=re.IGNORECASE | re.MULTILINE,
    )

    # Remove any leaked/plain-text version of that marker from search/AI output.
    text = re.sub(
        r"^\s*!?\s*generated[_\s-]*by\s*:\s*django[-_\s]*suggested[-_\s]*article\s*;?\s*article[_\s-]*id\s*:\s*[^\r\n]+\s*",
        "",
        text,
        flags=re.IGNORECASE | re.MULTILINE,
    )

    # Remove generated sync front matter from OpenKB summaries if it is ever
    # shown directly. Keep normal user-written front matter untouched.
    text = re.sub(
        r"^---\s*\n(?=[\s\S]*?generated[_\s-]*by\s*:\s*django-openkb-sync)(?:.|\n)*?---\s*\n?",
        "",
        text,
        flags=re.IGNORECASE,
    )

    return text.lstrip("\ufeff\n\r ")


